package com.epsmart.wzdp.activity.contract.fragment;

public class StringBean {
   public String gg;
   public String ff;
   public String aa;
   public String bb;
   public String cc;
   public String dd;
   public String ee;
  
public StringBean(String gg, String ff, String aa, String bb, String cc,
		String dd, String ee) {
	super();
	this.gg = gg;
	this.ff = ff;
	this.aa = aa;
	this.bb = bb;
	this.cc = cc;
	this.dd = dd;
	this.ee = ee;
}
public String getGg() {
	return gg;
}
public void setGg(String gg) {
	this.gg = gg;
}
public String getFf() {
	return ff;
}
public void setFf(String ff) {
	this.ff = ff;
}
public String getAa() {
	return aa;
}
public void setAa(String aa) {
	this.aa = aa;
}
public String getBb() {
	return bb;
}
public void setBb(String bb) {
	this.bb = bb;
}
public String getCc() {
	return cc;
}
public void setCc(String cc) {
	this.cc = cc;
}
public String getDd() {
	return dd;
}
public void setDd(String dd) {
	this.dd = dd;
}
public String getEe() {
	return ee;
}
public void setEe(String ee) {
	this.ee = ee;
}
   
}
